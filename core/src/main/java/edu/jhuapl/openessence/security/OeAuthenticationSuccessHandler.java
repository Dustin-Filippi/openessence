package edu.jhuapl.openessence.security;

import org.springframework.security.core.Authentication;
import org.springframework.security.web.WebAttributes;
import org.springframework.security.web.authentication.AuthenticationSuccessHandler;
import org.springframework.security.web.authentication.SimpleUrlAuthenticationSuccessHandler;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

public class OeAuthenticationSuccessHandler implements AuthenticationSuccessHandler {

    @Override
    public void onAuthenticationSuccess(HttpServletRequest request, HttpServletResponse response,
                                        Authentication authentication)
            throws IOException, ServletException {

        // If client asks to be redirected, then redirect them.
        // Some consumers, like Liferay, can't rely on client JS to navigate to homepage
        String reParam = request.getParameter("redirect");
        if (reParam != null && reParam.equals("true")) {
            response.sendRedirect(request.getContextPath() + "/oe/home/main");
        } else {
            response.setStatus(HttpServletResponse.SC_NO_CONTENT);
            // client doesn't need anything but 2xx status code to know auth was good

            // not sure if this is needed, but Spring's only AuthenticationSuccessHandler does this
            clearAuthenticationAttributes(request);
        }
    }

    /**
     * Copied from {@link SimpleUrlAuthenticationSuccessHandler}
     */
    private void clearAuthenticationAttributes(HttpServletRequest request) {
        HttpSession session = request.getSession(false);

        if (session == null) {
            return;
        }

        session.removeAttribute(WebAttributes.AUTHENTICATION_EXCEPTION);
    }

}
